    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if (conn == null) {
            JOptionPane.showMessageDialog(null, "Failed to connect to the database. Please check your connection settings.");
            return;
        }
        if(txt_username.getText().equals("") || txt_password.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter Username and Password");
        } else {
            // Implementasikan logika login di sini
            String sql = "SELECT * FROM users WHERE username=? AND password=?";
            try {
                ps = conn.prepareStatement(sql);
                ps.setString(1, txt_username.getText()); // Indeks parameter dimulai dari 1
                ps.setString(2, new String(txt_password.getPassword()));

                rs = ps.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Login Successfully");
                    this.dispose(); // Menutup form login
                    new users().show();
                } else {
                    JOptionPane.showMessageDialog(null, "Incorrect Username or Password");
                    txt_username.setText("");
                    txt_password.setText("");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage());
            } finally {
                // Menutup ResultSet dan PreparedStatement
                try {
                    if (rs != null) rs.close();
                    if (ps != null) ps.close();
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error closing resources: " + e.getMessage());
                }
            }
        }
    }                                         

