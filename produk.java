    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if(txt_idproduk.getText().equals("") || txt_nmproduk.getText().equals("") || 
            txt_harga.getText().equals("") || txt_stok.getText().equals("") || 
            txt_desk.getText().equals("")) 
         {
             JOptionPane.showMessageDialog(null, "All Data is required, please enter all data needed");
             resetForm();
         } else {
             try {
                 String id = txt_idproduk.getText();
                 String nama = txt_nmproduk.getText();
                 String harga = txt_harga.getText();
                 String stok = txt_stok.getText();
                 String deskripsi = txt_desk.getText();

                 String sql = "INSERT INTO products (product_id, product_name, price, stock, description) VALUES (?, ?, ?, ?, ?)";
                 ps = conn.prepareStatement(sql);
                 ps.setString(1, id);
                 ps.setString(2, nama);
                 ps.setString(3, harga);
                 ps.setString(4, stok);
                 ps.setString(5, deskripsi);

                 ps.executeUpdate(); // Menggunakan executeUpdate() untuk INSERT
                 JOptionPane.showMessageDialog(null, "Data inserted successfully");

                 products_Table();
                 resetForm();
             } catch (Exception e) {
                 JOptionPane.showMessageDialog(null, e);
             }
         }
    }                                         

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {                                         
        if (txt_idproduk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan ID Produk untuk mengedit data.");
        } else {
            try {
                String sql = "UPDATE products SET product_name = ?, price = ?, stock = ?, "
                        + "description = ? WHERE product_id = ?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, txt_nmproduk.getText());
                ps.setString(2, txt_harga.getText());
                ps.setString(3, txt_stok.getText());
                ps.setString(4, txt_desk.getText());
                ps.setString(5, txt_idproduk.getText());

                int rowsUpdated = ps.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(null, "Data produk berhasil diperbarui.");
                    products_Table(); // Memperbarui tampilan tabel setelah data diupdate
                    resetForm(); // Mengosongkan form setelah update
                } else {
                    JOptionPane.showMessageDialog(null, "Produk dengan ID tersebut tidak ditemukan.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }                                        

    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {                                           
        String keyword = txt_search.getText();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan kata kunci pencarian terlebih dahulu.");
        } else {
            try {
                String sql = "SELECT * FROM products WHERE product_name LIKE ? OR description LIKE ?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, "%" + keyword + "%");
                ps.setString(2, "%" + keyword + "%");
                rs = ps.executeQuery();
                tbl_produk.setModel(DbUtils.resultSetToTableModel(rs));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }                                          

