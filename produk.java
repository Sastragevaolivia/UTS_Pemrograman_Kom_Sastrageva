    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if (txt_idproduk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus dari tabel terlebih dahulu.");
        } else {
            int confirmation = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", 
                    "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

            if (confirmation == JOptionPane.YES_OPTION) {
                try {
                    String id = txt_idproduk.getText();

                    String sql = "DELETE FROM products WHERE product_id = ?";
                    ps = conn.prepareStatement(sql);
                    ps.setString(1, id);

                    int rowsAffected = ps.executeUpdate();

                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
                        products_Table(); // Refresh tabel
                        resetForm(); // Reset form
                    } else {
                        JOptionPane.showMessageDialog(null, "Data tidak ditemukan atau gagal dihapus.");
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }                                         

    private void tbl_produkMouseClicked(java.awt.event.MouseEvent evt) {                                        
        int selectedRow = tbl_produk.getSelectedRow();

        if (selectedRow != -1){

            // Mendapatkan nilai dari setiap kolom di baris yang dipilih
            String product_id = (tbl_produk.getValueAt(selectedRow, 0) != null) ? tbl_produk.getValueAt(selectedRow, 0).toString() : "";
            String product_name = (tbl_produk.getValueAt(selectedRow, 1) != null) ? tbl_produk.getValueAt(selectedRow, 1).toString() : "";
            String price = (tbl_produk.getValueAt(selectedRow, 2) != null) ? tbl_produk.getValueAt(selectedRow, 2).toString() : "";
            String stock = (tbl_produk.getValueAt(selectedRow, 3) != null) ? tbl_produk.getValueAt(selectedRow, 3).toString() : "";
            String description = (tbl_produk.getValueAt(selectedRow, 4) != null) ? tbl_produk.getValueAt(selectedRow, 4).toString() : "";

            txt_idproduk.setText(product_id);
            txt_nmproduk.setText(product_name);
            txt_harga.setText(price);
            txt_stok.setText(stock);
            txt_desk.setText(description);
        } else {
            JOptionPane.showMessageDialog(null, "Tidak ada baris yang dipilih");
        }
    }                                       

    
